document.getElementById("addButton1").onclick = function ()
{
  if (document.getElementById("addButton1").innerHTML == 'ADD')
  {
    document.getElementById("added1").innerHTML = 'Item has been added to your cart.';
    document.getElementById("addButton1").innerHTML = 'REMOVE';
  }
  else if (document.getElementById("addButton1").innerHTML == 'REMOVE')
  {
    document.getElementById("added1").innerHTML = 'Item has been removed from your cart.';
    document.getElementById("addButton1").innerHTML = 'ADD';
  }
};

